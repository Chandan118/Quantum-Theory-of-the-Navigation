# ─── Pauli / spin-½ matrices ────────────────────────────────────────────────
I2  = np.eye(2, dtype=complex)
Sx  = np.array([[0,    0.5 ],  [0.5,  0   ]], dtype=complex)
Sy  = np.array([[0,   -0.5j], [0.5j, 0   ]], dtype=complex)
Sz  = np.array([[0.5,  0   ],  [0,   -0.5 ]], dtype=complex)

def kron_N(*ops):
    result = ops[0]
    for op in ops[1:]:
        result = np.kron(result, op)
    return result

def make_spin_ops(n_qubits=4):
    ops = []
    for i in range(n_qubits):
        for S in (Sx, Sy, Sz):
            parts = [I2] * n_qubits
            parts[i] = S
            ops.append(kron_N(*parts))
    return [(ops[3*i], ops[3*i+1], ops[3*i+2]) for i in range(n_qubits)]

_spin_ops = make_spin_ops(4)
E1x, E1y, E1z = _spin_ops[0]
E2x, E2y, E2z = _spin_ops[1]
N1x, N1y, N1z = _spin_ops[2]
N2x, N2y, N2z = _spin_ops[3]

def build_hamiltonian(theta_deg, phi_deg=0.0,
                    omega_e=1.4,
                    A_tensor0=None, A_tensor1=None,
                    J=0.1):
    """
    Builds the Hamiltonian with full 3D magnetic field vector and hyperfine tensors.
    """
    theta = np.deg2rad(theta_deg)
    phi   = np.deg2rad(phi_deg)
    
    # Magnetic field vector in (x,y,z)
    bx = np.sin(theta) * np.cos(phi)
    by = np.sin(theta) * np.sin(phi)
    bz = np.cos(theta)
    
    # Zeeman term
    H = omega_e * (bx * (E1x + E2x) + by * (E1y + E2y) + bz * (E1z + E2z))
    
    # Hyperfine terms (Full 3x3 tensor support)
    if A_tensor0 is not None:
        E1_vec = [E1x, E1y, E1z]
        N1_vec = [N1x, N1y, N1z]
        for i in range(3):
            for j in range(3):
                H += A_tensor0[i, j] * (E1_vec[i] @ N1_vec[j])
                
    if A_tensor1 is not None:
        E2_vec = [E2x, E2y, E2z]
        N2_vec = [N2x, N2y, N2z]
        for i in range(3):
            for j in range(3):
                H += A_tensor1[i, j] * (E2_vec[i] @ N2_vec[j])
                
    # Exchange coupling
    H += J * (E1x @ E2x + E1y @ E2y + E1z @ E2z)
    return H

def build_singlet_projector():
    s = np.zeros(16, dtype=complex)
    for nuc in range(4):
        s[1 * 4 + nuc] += 1.0 / np.sqrt(2)
        s[2 * 4 + nuc] -= 1.0 / np.sqrt(2)
    return np.outer(s, s.conj())

P_S_GLOBAL = build_singlet_projector()

def lindblad_rhs(rho_flat, H, Ls, dim):
    rho = rho_flat.reshape(dim, dim)
    drho = -1j * (H @ rho - rho @ H)
    for L in Ls:
        Ld = L.conj().T
        drho += L @ rho @ Ld - 0.5 * (Ld @ L @ rho + rho @ Ld @ L)
    return drho.ravel()

def evolve_lindblad(rho0, H, Ls, tau=1.5, n_steps=300):
    dim = rho0.shape[0]
    t_eval = np.linspace(0, tau, n_steps)
    sol = solve_ivp(
        fun=lambda t, y: lindblad_rhs(y, H, Ls, dim),
        t_span=(0, tau),
        y0=rho0.ravel(),
        t_eval=t_eval,
        method='RK45',
        rtol=1e-8, atol=1e-11
    )
    return [sol.y[:, i].reshape(dim, dim) for i in range(sol.y.shape[1])], sol.t

def initial_singlet_state():
    singlet_e = np.array([[0,0,0,0],[0,0.5,-0.5,0],[0,-0.5,0.5,0],[0,0,0,0]], dtype=complex)
    return np.kron(singlet_e, np.eye(4, dtype=complex)/4.0)

def von_neumann_entropy(rho_full):
    # rho_full is 16x16; trace out nuclear (last 2 qubits, dim=4)
    rho_e = np.zeros((4, 4), dtype=complex)
    for i in range(4):   # nuclear basis
        rho_e += rho_full[i::4, i::4]
    eigvals = np.linalg.eigvalsh(rho_e)
    eigvals = eigvals[eigvals > 1e-15]
    return -np.sum(eigvals * np.log2(eigvals))

def l1_norm_coherence(rho):
    return np.sum(np.abs(rho) - np.abs(np.diag(np.diag(rho))))

class QuantumCore:
    def __init__(self, omega_e=1.4, J=0.1, gamma=0.05, tau=2.0, n_steps=500, temperature=0.0):
        self.omega_e = omega_e
        self.J = J
        self.gamma = gamma
        self.tau = tau
        self.n_steps = n_steps
        self.temperature = temperature
        
        # Default Cry4-like tensors
        self.A_tensor0 = np.diag([0.3, 0.3, 2.0])
        self.A_tensor1 = np.diag([0.2, 0.2, 1.5])
        self.P_S = P_S_GLOBAL

    def run_simulation(self, theta_deg, phi_deg=0.0, perturb=None):
        """Runs a single Lindblad trajectory, optionally with parameter perturbations."""
        A0 = self.A_tensor0
        A1 = self.A_tensor1
        if perturb is not None:
            A0 = A0 + np.random.normal(0, perturb, A0.shape)
            A1 = A1 + np.random.normal(0, perturb, A1.shape)
            
        H = build_hamiltonian(theta_deg, phi_deg, self.omega_e, A0, A1, self.J)
        
        # Lindblad dephasing
        gamma_eff = self.gamma * (1.0 + self.temperature/300.0) if self.temperature > 0 else self.gamma
        Ls = [np.sqrt(gamma_eff) * op[2] for op in _spin_ops]
        
        rho0 = initial_singlet_state()
        return evolve_lindblad(rho0, H, Ls, self.tau, self.n_steps)

    def get_stats(self, theta_deg, phi_deg=0.0, mc_samples=1, perturb=0.01):
        """Runs multiple samples to estimate mean and standard deviation of singlet yield."""
        yields = []
        for _ in range(mc_samples):
            traj, times = self.run_simulation(theta_deg, phi_deg, perturb=perturb if mc_samples > 1 else None)
            expects = np.real([np.trace(rho @ self.P_S) for rho in traj])
            phi_s = 2.0 * np.trapz(expects, times) / (3.0) # assuming kS=2, kT=1, normalisation
            yields.append(phi_s)
        return np.mean(yields), np.std(yields)

    def get_full_trajectory(self, theta_deg, phi_deg=0.0):
        return self.run_simulation(theta_deg, phi_deg)

